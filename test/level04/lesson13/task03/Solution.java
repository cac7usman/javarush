/* Рисуем треугольник
Используя цикл for вывести на экран прямоугольный треугольник из восьмёрок со сторонами 10 и 10.
Пример:
8
88
888
...
*/

public class Solution
{
    public static void main(String[] args) throws Exception
    {
        String s = "8";

        for (int a = 0; a < 1; a++)
        {
            System.out.print(s);
        }
        System.out.println();
        for (int a = 0; a < 2; a++)
        {
            System.out.print(s);
        }
        System.out.println();
        for (int a = 0; a < 3; a++)
        {
            System.out.print(s);
        }
        System.out.println();
        for (int a = 0; a < 4; a++)
        {
            System.out.print(s);
        }
        System.out.println();
        for (int a = 0; a < 5; a++)
        {
            System.out.print(s);
        }
        System.out.println();
        for (int a = 0; a < 6; a++)
        {
            System.out.print(s);
        }
        System.out.println();
        for (int a = 0; a < 7; a++)
        {
            System.out.print(s);
        }
        System.out.println();
        for (int a = 0; a < 8; a++)
        {
            System.out.print(s);
        }
        System.out.println();
        for (int a = 0; a < 9; a++)
        {
            System.out.print(s);
        }
        System.out.println();
        for (int a = 0; a < 10; a++)
        {
            System.out.print(s);
        }
        System.out.println();
    }
}


